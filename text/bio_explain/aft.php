「アートフェア東京」<br />
日程：2013.3.22 - 3.24<br />
会場：東京国際フォーラム<br />
<br />
日本最大のアート見本市「アートフェア東京」。東京 2013は、国内外の28 都市から 176軒のギャラリー・企業などが出展並びに協力参加し、古美術・工芸から、日本画、洋画、近・現代美術まで時代を越えた幅広いジャンルの作品が展示販売された。来場者は延べ 44,000 人を超え、大盛況のうちに閉幕。渋家は、作られてきたコミュニティやリレーションおよび活動拠点である渋家の所有の問題を取り扱う作品「Owner Change 」をThe Containerより2億5千万円で出品し、その様子は「NHKニュース7」で取り上げられブースには多くの来場者が訪れた。
