「ArtGigTokyo2 - 怖いよ、ママー! -」<br />
日程：2011.10.30<br />
会場：旧玉井病院<br />
<br />
廃業となっている東京・初台の玉井病院で行われた「幽霊」をテーマにしたアートイベント。このイベントは、国内外のアーティストが作品の展示やパフォーマンス、DJなどを行うもので「東京の公共の場で様々なアートをハプニングさせるプロジェクト」としてカナダ・トロント出身のキュレーター、シャイ・オハヨンによって企画されており、今回が2回目の開催だった。アーティストはテーマに沿った作品制作を行い、病院の壁、霊安室、和室、キッチン、放置された医療器具などを用いたサイトスペシフィックな作品が展示された。渋家は「渋家パーティー」のインダストリアル・ノイズを、半強制的に聞かせるパフォーマンスアート「Shibu-R-I」を披露した。
