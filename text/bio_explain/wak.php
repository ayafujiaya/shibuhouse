「わくわく SHIBUYA」 は、アーティストからアーティストへの紹介でどんどん出展者が増えていく展示だ。主催の「わくわくプロジェクト」は、2009年に「別府現代芸術フェスティバル2009 混浴温泉世界」の国内展として開催された「わくわく混浴アパートメント」をきっかけにスタートし、同世代のアーティストたちが表現者としての意思を共有し、日本中、世界中へと文化発信していくことを目的に全国各地でアートによって人と場をつなぐプロジェクトを行っている。この展示では、渋谷を舞台に全国から集結したアーティストが、作品展示や滞在制作、ライブパフォーマンスなどのイベントを繰り広げた。渋家は、蛇口からでてくるミッキーの物真似などのパフォーマンスを行った。<br />
