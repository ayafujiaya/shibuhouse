東京の秋恒例のディレクターズ・アートフェア「ULTRA」。40歳以下のギャラリスト個人を出展単位とする新しい形のアートフェアであり、ギャラリーで日々実際に作家と対話し、作品の選定を行うディレクターにスポットをあて、彼らの新鮮な視点で選ばれた、まさに「いま」を切り取った現代アート作品が多数集まった。作品を購入できるアートフェアだが、入場料はなく、展覧会と同様スパイラルガーデンのオープンスペースで、気軽に作品に触れることができる。また気になる作品についてディレクターに直接質問することも可能。渋家は4th houseで撮影した2枚の集合写真「House Moving」を出展。
