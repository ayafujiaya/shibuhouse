「渋家トリエンナーレ」<br />
日程：2010.11.20 - 11.22<br />
会場：3rd house<br />
<br />
渋家の活動開始3年目に開催した、生成されたコミュニティによって展示を行う「トリエンナーレ」。渋家メンバーだけでなく、渋家と関係性のあるアーティストあわせて10人が参加。もともとは2nd houseを巨大な布で覆った作品「BAD CROSS」の内部で開催予定だったが、苦情により引っ越しを余儀なくされ、3rd houseへの引っ越し直後、家の中心に「BAD CROSS」を展示して開催。展示のほか、7つのトークイベントを企画し、3日間で約200人が来場した。
