『朝日ジャーナル』<br />
記事名：「特集&nbsp;&nbsp;日本破壊計画 – シェアハウスの人々 -」<br />
掲載号：2011.3.19号<br />
<br />
発刊直前に大震災が起き、特集タイトルが問題視された『朝日ジャーナル』復刊号。『朝日ジャーナル』は、1960年代から1970年代に隆盛をきわめた雑誌であり「怒りの復活」と銘打たれた復刊号には、格差や貧困に象徴される、日本型社会システムの破綻をテーマに、さまざまな論客が論考を寄せた。 渋家は4Pにわたるルポルタージュが掲載される。
