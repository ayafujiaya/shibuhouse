「VIVA☆校」<br />
日程：2010.12.27 <br />
会場：棚ガレリ<br />
<br />
私たちは、まずギャラリーとその近くの部屋、双方に監視カメラを設置し、相互に監視し合えるようにしました。そして部屋の扉にはバリケードを築いて出入りを禁じ、渋家メンバーが内部を占拠して日常生活を営むのです。ギャラリーの来場者は、監視カメラと繋がったディスプレイを通じて渋家のダラダラとした日常を覗き見ることになり、同時に渋家メンバーからの視線に晒されることにもなります。（中略）実際に展覧会では、渋家側と来場者との、言語を介さない、身振り手振りによるコミュニケーションが丸一日続けられ、コミュニケーションの根源的な愉悦と困難が同時に経験されることとなりました。（文/中島晴矢）
