「BAR☆Time Line」<br />
日程：2010.3.28&nbsp;&nbsp; <br />
会場：3331 Arts Chiyoda<br />
<br />
秋葉原から全国の学生に向けて発信される学生メディアセンター•なないろチャンネルが主催のライブイベント「BAR☆Time Line」。なないろチャンネルはさまざな学生の作品やアイデア、活動を集積し、展示や制作、アーティスト紹介などを行っている団体だ。渋家は、モーニング娘。の「ザ☆ピ～ス！」を流し、1つのステージ上で6人のパフォーマーが、それぞれ、全身にトイレペッパーで巻き奇声をあげながら暴れる、フローラルキャンディーという名の女装者になり歌唱する、全裸の状態で段ボールにしまわれライブの途中で突如出てくる、今まで影響を受けた人の名前が書かれたスケッチブックをめくる、ブレイクダンスを踊る、というパフォーマンスを行った。<br />
